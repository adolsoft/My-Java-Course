package day18_nestedForLoop_whileLoop;

public class C03_WhileLoop {

	public static void main(String[] args) {
		// 20'den 30'a kadar olan sayilari(20 ve 30 dahil)
        // while loop ile yazdirin
        

	int sayi1=20;
	int sayi2=30;
	
	
	while (sayi1<=sayi2) {
		System.out.print(sayi1 + " ");
		sayi1++;
	} 
		
	}

}
